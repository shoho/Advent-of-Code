def execute arr
	i = 0
	while arr[i] != 99 do
		operation_code = arr[i]

		if operation_code == 1
			arr[arr[i+3]] = arr[arr[i+1]] + arr[arr[i+2]]
			i = i+4

		elsif operation_code == 2
			arr[arr[i+3]] = arr[arr[i+1]] * arr[arr[i+2]]
			i = i+4

		elsif operation_code == 3
			arr[arr[i+1]] = arr[i+1]
			i = i+2

		elsif operation_code == 4
			print arr[arr[i+1]]
			i = i+2

		else
			operation_code = arr[i].to_s[-2..-1].to_i

			x_parameter_mode = (arr[i].to_s[-3] == nil) ? 0 : arr[i].to_s[-3].to_i
			y_parameter_mode = (arr[i].to_s[-4] == nil) ? 0 : arr[i].to_s[-4].to_i

			x = (x_parameter_mode == 0) ? arr[arr[i+1]] : arr[i+1]
			y = (y_parameter_mode == 0) ? arr[arr[i+2]] : arr[i+2]

			location = arr[i+3]

			arr[location] = x + y if operation_code == 1
			arr[location] = x * y if operation_code == 2

			i = i+4
		end
	end
	arr
end

def runTest arr,output
